import type { NextPage } from 'next'

import Head from 'next/head'
import { PrismaClient } from '@prisma/client'
import { useState } from 'react'
import Create from '../components/Create'
import List from '../components/List'
import { Quote } from '../types/types'

const prisma = new PrismaClient()

export const getStaticProps = async () => {
  const initialQuotes: Quote[] = await prisma.quote.findMany()
  return { 
    props: { initialQuotes }
  }
}

const Home: NextPage<{initialQuotes: Quote[]}> = ({ initialQuotes }) => {
  const [quotes, setQuotes] = useState<Quote[]>(initialQuotes)

  const moreQuotes = (addedQuote: Quote) => {
    setQuotes([...quotes, addedQuote])
  }

  const removeQuote = (removedQuote: Quote) => {
    setQuotes(quotes.filter((item) => item.id !== removedQuote.id))
  }

  return (
    <>
      <Head>
        <title>CRUD APP - Collection of quotes</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <style jsx global>{`
        body {
          background: #222;
          color: #fff;
          margin: 0;
          font-family: cursive;
        }
      `}</style>

      <style jsx>{`
        main {
          max-width: 600px;
          margin: auto;
          display: flex;
          flex-direction: column;
          min-height: 100vh;
          background: royalblue;
        }
      `}</style>

      <main>
        <List quotes={quotes} removeQuote={removeQuote} />

        <Create moreQuotes={moreQuotes} />
      </main>
    </>
  )
}

export default Home
